    def __getitem__(self, index):
        s_begin = index
        s_end = s_begin + self.seq_len
        r_begin = s_end - self.label_len
        r_end = r_begin + self.label_len + self.pred_len

        seq_x = self.data_x[s_begin:s_end]
        seq_y = self.data_y[r_begin:r_end]
        seq_x_mark = self.data_stamp[s_begin:s_end]
        seq_y_mark = self.data_stamp[r_begin:r_end]

        # we need to scale the data for each sample here if we use rolling normalization
        if self.normalization_method == 'rolling':
            # print('seq_x', seq_x.shape, 'seq_y', seq_y.shape, 'seq_x_mark', seq_x_mark.shape, 'seq_y_mark',
            #       seq_y_mark.shape,'self.scaler', len(self.scaler))
            seq_x_features = seq_x[:, :-self.target_dim]
            seq_x_targets = seq_x[:, -self.target_dim:]
            seq_x_features = self.scaler[s_begin].transform(seq_x_features)
            seq_x = np.concatenate((seq_x_features, seq_x_targets), axis=1)
            seq_y_features = seq_y[:, :-self.target_dim]
            seq_y_targets = seq_y[:, -self.target_dim:]
            seq_y_features = self.scaler[r_begin].transform(seq_y_features)
            seq_y = np.concatenate((seq_y_features, seq_y_targets), axis=1)
        # print('seq_x.shape', seq_x.shape, 'seq_y.shape', seq_y.shape, 'seq_x_mark.shape', seq_x_mark.shape,
        #       'seq_y_mark.shape', seq_y_mark.shape)
        return seq_x, seq_y, seq_x_mark, seq_y_mark

    def __len__(self):
        return len(self.data_x) - self.seq_len - self.pred_len + 1

    def inverse_transform(self, data):
        # print('data.shape', data.shape)
        if self.normalization_method == 'standard':
            return self.scaler.inverse_transform(data)
        elif self.normalization_method == 'rolling':
            # we need to inverse transform the data for each sample
            data_inverse = np.zeros(data.shape)
            for i in range(data.shape[0]):
                data_inverse[i, :-self.target_dim] = self.scaler[i].inverse_transform(data[i, :-self.target_dim])
                data_inverse[i, -self.target_dim:] = data[i, -self.target_dim:]
            return data_inverse
        else:
            raise ValueError(f"normalization method {self.normalization_method} is not supported")

    def recover_close_price_from_return(self, returns):
        # df['ret1'] = df['close'].pct_change(1).shift(-1)
        recovered_close_price = np.zeros(returns.shape)
        # print('close_price.shape', self.close_price.shape)
        close_price = self.close_price.values
        recovered_close_price[0] = close_price[0]
        for i in range(1, returns.shape[0]):
            recovered_close_price[i] = close_price[i - 1] * (1 + returns[i - 1])
        # cast recovered_close_price from (x,1,1) to (x,)
        recovered_close_price = recovered_close_price.reshape(-1)
        return recovered_close_price

