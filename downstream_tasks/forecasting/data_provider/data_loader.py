        # reset the label_len
        if self.features_name:
            cols = self.features_name
        else:
            cols = list(stocks_df.columns)
            cols.remove(self.target)
            cols.remove('date')
        # the mov1 is a binary target, so we need to one-hot encode it
        if self.target == 'mov1':
            # get the unique values of the target
            self.target_dim = len(stocks_df[self.target].unique())
            # one-hot encode the target
            # print('stocks_df', stocks_df.shape,stocks_df.iloc[0,:])
            target_one_hot_encoding = pd.get_dummies(stocks_df, columns=[self.target], dtype=float).iloc[:,
                                      -self.target_dim:]
            # print('stocks_df',stocks_df.shape, stocks_df.iloc[0,:])
            # concatenate the one-hot encoded target with the original dataframe
            df_raw = pd.concat([stocks_df[['date'] + cols], target_one_hot_encoding], axis=1)
        else:
            df_raw = stocks_df[['date'] + cols + [self.target]]
            self.target_dim = 1
        # print('df_raw', df_raw.shape)

        num_train = int(len(df_raw) * 0.6)
        num_test = int(len(df_raw) * 0.2)
        num_vali = len(df_raw) - num_train - num_test
        if self.normalization_method == 'rolling':
            border1s = [self.seq_len, num_train - self.seq_len, len(df_raw) - num_test - self.seq_len]
        else:
            border1s = [0, num_train - self.seq_len, len(df_raw) - num_test - self.seq_len]
        # since the last return is nan, we need to remove it
        border2s = [num_train, num_train + num_vali, len(df_raw) - 1]
        border1 = border1s[self.set_type]
        border2 = border2s[self.set_type]

        if self.features == 'M' or self.features == 'MS':
            cols_data = df_raw.columns[1:]
            df_data = df_raw[cols_data]
        elif self.features == 'S':
            df_data = df_raw[[self.target]]

        # cols_data = df_raw.columns[1:]
        # df_data = df_raw[cols_data]
        close_price = df_raw['close']
        if self.scale:
            # no need to scale the target(last self.target_dim) as it is already a return
            if self.normalization_method == 'standard':
                # only use the train set to fit the scaler
                train_data = df_data[border1s[0]:border2s[0]]
                targets = df_data.iloc[:, -self.target_dim:]
                train_feature = train_data.iloc[:, :-self.target_dim]
                self.scaler.fit(train_feature.values)
                features = df_data.iloc[:, :-self.target_dim]
                features_scaled = self.scaler.transform(features.values)
                data = np.concatenate((features_scaled, targets), axis=1)
            elif self.normalization_method == 'rolling':
                # for each sample, use the last seq_len data to normalize the data
                self.scaler = []
                for i in range(border1s[self.set_type], border2s[self.set_type]):
                    sample_lookback_features = df_data.iloc[i - self.seq_len:i, :-self.target_dim]
                    scaler = StandardScaler()
                    scaler.fit(sample_lookback_features)
                    self.scaler.append(scaler)
                data = df_data.values
            else:
                raise ValueError(f"normalization method {self.normalization_method} is not supported")
        else:
            data = df_data.values
        # check whether the data is correctly scaled
        # print("data_scaled", data_scaled)
        # print("data", data)


        df_stamp = df_raw[['date']][border1:border2]
        df_stamp['date'] = pd.to_datetime(df_stamp.date)
        if self.timeenc == 0:
            df_stamp['month'] = df_stamp.date.apply(lambda row: row.month, 1)
            df_stamp['day'] = df_stamp.date.apply(lambda row: row.day, 1)
